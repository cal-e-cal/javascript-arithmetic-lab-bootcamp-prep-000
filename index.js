function add(a,b) {
  sum = a + b 
  return sum
}

function subtract(a,b) {
  difference = a - b
  return difference
}

function multiply(a,b) {
  product = a * b
  return product
}

function divide(a,b) {
  quotient = a / b
  return quotient
}

function inc(n) {
  n++
  return n 
}

function dec(n) {
  n--
  return n 
}

function makeInt(n) {
  n = parseInt(n,10)
  return n 
}

function preserveDecimal(n) {
  n = parseFloat(n)
  return n 
}